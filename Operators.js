//Arithmetic Operators

// 1.Additon->adds two numbers-> +
let num1=10;
let num2=20;
console.log(num1+num2);

//Subtraction->subtracts two numbers-> -
console.log(num1-num2);

//Multiplication-> * -> multiplies numbers
console.log(num1*num2);

//division operators ->  /-> divides two numbers
console.log(num1/num2);

//modulo/modulus -> % ->gives you the remainder
console.log(num1%num2);

//power operator-> **-> raises a number to a power
console.log(num1**num2);

//post increment
var x=5;
x++;
console.log(x);

//post decrement
var y=10;
y--;
console.log(y);

//assignment operators
let a=10;
let b=20;
console.log(a+=b);//a=a+b;
console.log(a-=b);//a=a-b;
console.log(a/=b);//a=a/b;
console.log(b%=a);//b=b%a;
console.log(a*=b);//a=a*b;

//comparision or relational
//== -> linient it only checks value
var value1=5;//number
var value2='5';//string
var value3=50;

console.log(value1==value2);//true
console.log(value1===value2);//false
console.log(value1!=value2);//false
console.log(value1!==value2);//true
console.log(value1>=value3);//false
console.log(value1>value3);//false
console.log(value1<=value3);//true
console.log(value1<value3);//true

